package naveenJava1;

public class IncrementDecrementExample {

	public static void main(String[] args) {

		int i = 1;				
		System.out.println(i);
		int j = i++;
		System.out.println(i);
		System.out.println(j);
		
		int a = 1;
		int b = ++a;
		System.out.println(a);
		System.out.println(b);
	} 
} 